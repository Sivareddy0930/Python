# The word "polymorphism" means "many forms", and in programming it refers to methods/functions/operators with the same name
# that can be executed on many objects or classes.


# Polymorphic len() function

# There are some functions in Python which are compatible to run with multiple data types.
#
# One such function is the len() function. It can run with many data types in Python. Let's look at some example use cases of the function.

# example:-  len(list),len(string),len(tuple)
# len() is a method with same name that can be executed on many objects like list,string,tuple etc....

list =[1,2,3,4]
print(len(list))

tuple=(5,6,7,8)
print(len(tuple))
